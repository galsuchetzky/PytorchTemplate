import os
import logging
from pathlib import Path
from functools import reduce, partial
from operator import getitem
from datetime import datetime
from logger.logger import setup_logging
from utils.util import read_json, write_json
from importlib import import_module


class ConfigParser:
    def __init__(self, config, resume=None, modification=None, run_id=None):
        """
        class to parse configuration json file. Handles hyper-parameters for training, initializations of modules,
        checkpoint saving and logging module.
        :param config:          Dict containing configurations, hyper-parameters for training. contents of
                                `config.json` file for example.
        :param resume:          String, path to the checkpoint being loaded.
        :param modification:    Dict keychain:value, specifying position values to be replaced from config dict.
        :param run_id:          Unique Identifier for training processes. Used to save checkpoints and training log.
                                Timestamp is being used as default
        """
        # load config file and apply modification
        self._config = _update_config(config, modification)
        self.resume = resume

        # set save_dir where trained model and log will be saved.
        save_dir = Path(self.config['trainer']['save_dir'])

        exper_name = self.config['name']
        if run_id is None:  # use timestamp as default run-id
            run_id = datetime.now().strftime(r'%m%d_%H%M%S')
        self._save_dir = save_dir / 'models' / exper_name / run_id
        self._log_dir = save_dir / 'log' / exper_name / run_id
        self._predictions_dir = save_dir / 'predictions' / exper_name / run_id
        self.predictions_file_name = self._predictions_dir / 'predictions.csv'

        # make directory for saving checkpoints and log.
        exist_ok = run_id == ''
        self.save_dir.mkdir(parents=True, exist_ok=exist_ok)
        self.log_dir.mkdir(parents=True, exist_ok=exist_ok)
        self.predictions_dir.mkdir(parents=True, exist_ok=exist_ok)

        # save updated config file to the checkpoint dir
        write_json(self.config, self.save_dir / 'config.json')

        # configure logging module
        setup_logging(self.log_dir)
        self.log_levels = {
            0: logging.WARNING,
            1: logging.INFO,
            2: logging.DEBUG
        }

    @classmethod
    def from_args(cls, args, options=''):
        """
        Initialize this class from some cli arguments. Used in train, test.
        :param args: Arguments from which to initialize the configuration.
        :param options: additional options to add to the config.
        :return: ConfigParser object configured with the arguments.
        """

        # Update the arguments with the options.
        for opt in options:
            args.add_argument(*opt.flags, default=None, type=opt.type)
        if not isinstance(args, tuple):
            args = args.parse_args()

        # Set cuda device.
        if args.device is not None:
            os.environ["CUDA_VISIBLE_DEVICES"] = args.device

        # Set training resume file.
        if args.resume is not None:
            resume = Path(args.resume)
            cfg_fname = resume.parent / 'config.json'
        else:
            msg_no_cfg = "Configuration file need to be specified. Add '-c config.json', for example."
            assert args.config is not None, msg_no_cfg
            resume = None
            cfg_fname = Path(args.config)

        config = read_json(cfg_fname)
        if args.config and resume:
            # update new config for fine-tuning
            config.update(read_json(args.config))

        config['debug'] = args.debug

        # parse custom cli options into dictionary
        modification = {opt.target: getattr(args, _get_opt_name(opt.flags)) for opt in options}

        ConfigParser.resolve_constants(config)

        return cls(config, resume, modification)

    @classmethod
    def resolve_constants(cls, config, constants=None):
        """
        Resolves all the json constants.
        :param config: The configuration file or a sub part of it (this function is recursive).
        :param constants: The constants to use for resolving.
        """
        # If in the upper layer or constants are configured in the subpart.
        if 'constants' in config:
            constants = config['constants']

        # If no constants are configured, return.
        if not constants:
            return

        # Resolve the constants.
        for key in config:
            if isinstance(config[key], str) and config[key].startswith('constants.'):
                config[key] = constants[key.replace('constants.', '')]
            elif isinstance(config[key], dict):
                ConfigParser.resolve_constants(config[key], constants)

    def init_obj(self, name, module=None, *args, **kwargs):
        """
        Finds a function handle with the name given as 'type' in config, and returns the
        instance initialized with corresponding arguments given.

        `object = config.init_obj('name', module, a, b=1)`
        is equivalent to
        `object = module.name(a, b=1)`
        """
        if 'module' not in self[name]:
            self[name]['module'] = module.__name__

        return ConfigParser.init_obj_recursive(self.config, name, *args, **kwargs)

    @staticmethod
    def init_obj_recursive(module_dict, name, *args, **kwargs):
        """
        Performs recursive initialization of the object arguments and creates the object.
        See init_obj().
        :param module_dict: A dictionary containing elements which are types and arguments (see config.json for
        example)
        :param name: The name of the attribute.
        :param args: additional arguments.
        :param kwargs: additional arguments.
        :return: The initialized object.
        """
        assert 'module' in module_dict[name], "module not specified for " + name

        # Get the module in which the object is defined.
        module = import_module(module_dict[name]['module'])

        # Get the object name.
        module_name = module_dict[name]['type']

        # Get the object arguments and perform recursive resolving.
        tmp_args = dict(module_dict[name]['args'])
        module_args = {}
        for arg in tmp_args:
            if isinstance(tmp_args[arg], dict):
                module_args[arg] = ConfigParser.init_obj_recursive(tmp_args, arg, *args, **kwargs)
            else:
                module_args[arg] = tmp_args[arg]

        assert all([k not in module_args for k in kwargs]), 'Overwriting kwargs given in config file is not allowed'
        module_args.update(kwargs)

        # Initiates and returns the object.
        return getattr(module, module_name)(*args, **module_args)

    def init_ftn(self, name, module, *args, **kwargs):
        """
        Finds a function handle with the name given as 'type' in config, and returns the
        function with given arguments fixed with functools.partial.

        `function = config.init_ftn('name', module, a, b=1)`
        is equivalent to
        `function = lambda *args, **kwargs: module.name(a, *args, b=1, **kwargs)`.
        """
        module_name = self[name]['type']
        module_args = dict(self[name]['args'])
        assert all([k not in module_args for k in kwargs]), 'Overwriting kwargs given in config file is not allowed'
        module_args.update(kwargs)
        return partial(getattr(module, module_name), *args, **module_args)

    def __getitem__(self, name):
        """Access items like ordinary dict."""
        return self.config[name]

    def get_logger(self, name, verbosity=2):
        """
        Gets a logger from the loggers.
        :param name: The name of the logger to get.
        :param verbosity: The verbosity level of the logger.
        :return: The logger.
        """
        msg_verbosity = 'verbosity option {} is invalid. Valid options are {}.'.format(verbosity,
                                                                                       self.log_levels.keys())
        assert verbosity in self.log_levels, msg_verbosity
        logger = logging.getLogger(name)
        logger.setLevel(self.log_levels[verbosity])
        return logger

    def get_predictions_file_name(self):
        return self.predictions_file_name

    # setting read-only attributes
    @property
    def config(self):
        return self._config

    @property
    def save_dir(self):
        return self._save_dir

    @property
    def log_dir(self):
        return self._log_dir

    @property
    def predictions_dir(self):
        return self._predictions_dir


# helper functions to update config dict with custom cli options
def _update_config(config, modification):
    if modification is None:
        return config

    for k, v in modification.items():
        if v is not None:
            _set_by_path(config, k, v)
    return config


def _get_opt_name(flags):
    for flg in flags:
        if flg.startswith('--'):
            return flg.replace('--', '')
    return flags[0].replace('--', '')


def _set_by_path(tree, keys, value):
    """Set a value in a nested object in tree by sequence of keys."""
    keys = keys.split(';')
    _get_by_path(tree, keys[:-1])[keys[-1]] = value


def _get_by_path(tree, keys):
    """Access a nested object in tree by sequence of keys."""
    return reduce(getitem, keys, tree)
